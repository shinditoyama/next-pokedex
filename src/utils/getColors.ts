export function getColors(color: number) {
  switch (color) {
    case 1:
      return "black";
    case 2:
      return "blue";
    case 3:
      return "brown";
    case 4:
      return "gray";
    case 5:
      return "green";
    case 6:
      return "pink";
    case 7:
      return "purple";
    case 8:
      return "red";
    case 9:
      return "white";
    case 10:
      return "yellow";
    default:
      return "";
  }
}
